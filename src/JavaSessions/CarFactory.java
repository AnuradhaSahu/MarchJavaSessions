package JavaSessions;

public class CarFactory {

	String name;
	int price;
	String color;
	char isAutomatic;
	boolean isLaunched;
	public CarFactory(String name, int price, String color, char isAutomatic, boolean isLaunched) {
		this.name = name;
		this.price = price;
		this.color = color;
		this.isAutomatic = isAutomatic;
		this.isLaunched = isLaunched;
	}
	
}
